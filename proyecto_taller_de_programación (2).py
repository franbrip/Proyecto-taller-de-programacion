def visualizacion_inventario():
    print("Inventario ")
    if len(inventario) == 0: #si el tamaño del diccionario es 0, esta vacío
        print("No hay equipos registrados.")
        return

    lista_equipos = [] #se crea una lista vacía
    for ip in inventario: #pasamos los datos del diccionario a una lista para iterar
        lista_equipos.append(inventario[ip])

    for disp in lista_equipos: # se recorre la lista uno por uno
        ip_protegida = ocultar_ip(disp.ip) #llama la función para ocultar la ip

        if disp.estado == True: #transforma el valor booleano a un texto para el usuario
            texto_estado = "Activo"
        else:
            texto_estado = "Inactivo"

        # guardamos los datos ordenados en una estructura de Tupla
        datos_dispositivo = (disp.nombre, disp.marca, disp.modelo, ip_protegida, texto_estado)

        # Imprimimos accediendo a las posiciones de la tupla
        print("Nombre:", datos_dispositivo[0], "| Marca:", datos_dispositivo[1], "| Modelo:", datos_dispositivo[2], "| IP:", datos_dispositivo[3], "| Estado:", datos_dispositivo[4])

    lista_marcas = [] #para guardas las marcas ya encontradas
    for disp in lista_equipos:
        if disp.marca not in lista_marcas: # analiza si la marca del equipo actual no esta en la lista
            lista_marcas.append(disp.marca) #se agrega a la lista

    print("Fabricantes únicos en la empresa:", len(lista_marcas)) #muestra el total de marcas sin repetir
    for m in lista_marcas:
        print("-", m)
