#Función 2.10
def cargar_datos():
    if not os.path.exists("Inventario.txt"):  #revisa si el archivo existe
        print("El archivo Inventario no existe. Se creara uno nuevo al guardar.")
        return

    archivo = open("Inventario.txt", "r") #archivo en modo lectura
    try:
        for linea in archivo: #recorre línea por línea
            linea = linea.strip() #quita los saltos de línea y espacios
            if linea != "": #si la línea no esta completamente vacía se ejecuta
                datos = linea.split(";")
                nombre = datos[0]
                marca = datos[1]
                modelo = datos[2]
                ip = datos[3]

                if datos[4] == "True": #si esta activo, el estado saldrá con un texto llamado true, en caso contrario false
                    estado = True
                else:
                    estado = False

                nuevo_dispositivo = Dispositivo(nombre, marca, modelo, ip, estado)
                inventario[ip] = nuevo_dispositivo #se guarda en el diccionario, usando la ip como llave
        print("Datos cargados correctamente.")
    finally:
        archivo.close()

