def validar_texto(texto, nombre_del_campo):
    # Esta función revisa que el texto no esté vacío
    # nombre_del_campo es solo para mostrar mensajes más claros
    # Por ejemplo: "nombre", "marca" o "modelo"

    # Paso 1: Se detecta si el usuario no escribrio nada y si solo puso espacios en blanco
    if texto == "" or texto.strip() == "":
        print("Error: el campo '" + nombre_del_campo + "' no puede estar vacío.")
