# Guarda los datos del diccionario utilizando try-finally en un archivo txt
def guardar_en_archivo():
    archivo = open("Inventario.txt", "w") #abre el archivo en modo escritura
    try:
        for ip in inventario: #recorre las ip guardada en el diccionario inventario
            disp = inventario[ip]
            linea = disp.nombre + ";" + disp.marca + ";" + disp.modelo + ";" + disp.ip + ";" + str(disp.estado) + "\n"
            archivo.write(linea) #escribe la línea generada dentro del archivo
        print("Datos guardados en Inventario correctamente.")
    finally:
        archivo.close()

