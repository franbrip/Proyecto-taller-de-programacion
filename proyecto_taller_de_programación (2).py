#función 2.3
def pedir_ip():
    # Esta función le pide la IP al usuario y no para hasta que ingrese una válida

    while True:
        ip = input("Ingrese la dirección IP (ejemplo: 192.168.1.10): ")

        # Llamamos a validar_ip para saber si lo que escribió está bien
        if validar_ip(ip) == True:
            # Si es válida, salimos del bucle y devolvemos la IP
            return ip
        else:
            # Si no es válida, avisamos y volvemos a preguntar
            print("La IP ingresada no es válida. Intente de nuevo.")
            print()
