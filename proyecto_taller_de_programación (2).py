#  FUNCIÓN 2.2 - Validar formato de IP

def validar_ip(ip):
    #Se tomara como ejemplo la IPv4 osea:192.168.1.10
    # Tiene 4 números separados por puntos

    # Paso 1: Separar la IP en partes usando el punto como separador
    # Toma el texto y lo separa tras cada "." quedando una lista de string , esto se guarda en una variable llamada segmentos
    segmentos = ip.split(".") # split solo para texto

    # Paso 2:Luego se cuenta que tenga 4 partes
    if len(segmentos) != 4:
        print("Error: la IP debe tener exactamente 4 números separados por puntos.")
        return False

    # Paso 3: Revisar cada una de las 4 partes
    for segmento in segmentos:

        # toma cada elemento de la lista creada y verifica que sea un numero
        if segmento.isdigit() == False:
            print("Error: cada parte de la IP debe ser un número.")
            return False

        # Convertir el texto a número para poder hacer matematicas
        numero = int(segmento)

        #analiza si el numero de cada parte de "segmento" esta entre el rango 0 a 255
        if numero < 0 or numero > 255:
            print("Error: cada número de la IP debe estar entre 0 y 255.")
            return False

    # Si pasa por los tres filtros es una IP es válida
    return True
