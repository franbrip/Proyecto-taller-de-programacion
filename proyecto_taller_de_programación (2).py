# para ocultar los últimos dos segmentos de la IP
def ocultar_ip(ip):
    partes = ip.split(".") #corta la ip en cada punto, genera una lista de 4 partes
    if len(partes) == 4:  # 1° estructura de control (if)
        nueva_ip = partes[0] + "." + partes[1] + ".*.*" #añade astericos a las 2 primeras partes
        return nueva_ip #devuelve una ip parcialmente oculta
    return ip #si falla algo devuelve la ip original

